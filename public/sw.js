// Advanced Service Worker - SkyTemp
const CACHE_NAME = 'skytemp-advanced-v1';
const urlsToCache = [
  '/',
  '/manifest.json',
  '/weather_icon.png'
];

// Install event
self.addEventListener('install', (event) => {
  console.log('ðŸš€ Service Worker installing...');
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then((cache) => {
        console.log('ðŸ“¦ Opened cache');
        return cache.addAll(urlsToCache);
      })
      .then(() => {
        console.log('âœ… All resources cached');
        return self.skipWaiting();
      })
  );
});

// Activate event
self.addEventListener('activate', (event) => {
  console.log('âœ… Service Worker activated!');
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (cacheName !== CACHE_NAME) {
            console.log('ðŸ—‘ï¸ Deleting old cache:', cacheName);
            return caches.delete(cacheName);
          }
        })
      );
    }).then(() => self.clients.claim())
  );
});

// Fetch event with cache fallback
self.addEventListener('fetch', (event) => {
  // Skip non-GET requests
  if (event.request.method !== 'GET') return;

  event.respondWith(
    caches.match(event.request)
      .then((response) => {
        // Return cached version if found
        if (response) {
          return response;
        }

        // Otherwise fetch from network
        return fetch(event.request)
          .then((response) => {
            // Check if we received a valid response
            if (!response || response.status !== 200 || response.type !== 'basic') {
              return response;
            }

            // Clone the response
            const responseToCache = response.clone();

            // Add to cache for future visits
            caches.open(CACHE_NAME)
              .then((cache) => {
                cache.put(event.request, responseToCache);
              });

            return response;
          })
          .catch(() => {
            // Optional: Return offline page here
            console.log('ðŸŒ Network request failed:', event.request.url);
          });
      })
  );
});

